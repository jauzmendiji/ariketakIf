import java.util.Scanner;

public class OperacionesIf {

	public static void main (String args[]) {

	int num1, num2;

	Scanner preg = new Scanner(System.in);
	System.out.print("\nInserta el primer numero: ");
	num1 = preg.nextInt();
	System.out.print("\nInserta el segundo numero: ");
	num2 = preg.nextInt();


	if (num1 == num2){

	System.out.print("\nLos numeros son iguales ");		
	
		}

	if (num1 != num2){

	System.out.print("\nLos numeros no son iguales ");		
	
		}

	if (num1 < num2){

	System.out.print("\nEl primer numero es MENOR que el segundo ");		
	
		}

	if (num1 > num2){

	System.out.print("\nEl primer numero es MAYOR que el segundo ");		
	
		}

	if ((num1 > 0) && (num2 > 0)){

	System.out.print("\nAmbos numeros son MAYORES que cero ");		
	
		}

	if ((num1 < 0)||(num2 < 0)){

	System.out.print("\nUno, o los dos, numeros son MENORES que cero ");		
	
		}
	}
}
